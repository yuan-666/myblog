export function getCategoryLabel(cat: Version['category'], locale: string): string {
  const map: Record<string, Record<string, string>> = {
    zh: { feature: '新功能', fix: '修复', improve: '优化', infra: '架构' },
    en: { feature: 'Feature', fix: 'Fix', improve: 'Improve', infra: 'Infra' },
  }
  return map[locale]?.[cat] || map.en[cat]
}

export function getCategoryColor(cat: Version['category']): string {
  const colors: Record<string, string> = {
    feature: 'linear-gradient(135deg, #6366f1, #8b5cf6)',
    fix: 'linear-gradient(135deg, #ef4444, #f97316)',
    improve: 'linear-gradient(135deg, #10b981, #06b6d4)',
    infra: 'linear-gradient(135deg, #64748b, #94a3b8)',
  }
  return colors[cat]
}
